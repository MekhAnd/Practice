package com.gmail.mekhanich.andrii;

public class Main {

	public static void main(String[] args) {

		// Таблица умножения на 5
		
		int result;										//declaring the variable
		for (int i = 0; i<=10; i++) {  					//start cycle 
			result = i * 5;								// calculating the variable
			System.out.println( i+" * 5 = "+result);	// return the result	
		}

	}

}
